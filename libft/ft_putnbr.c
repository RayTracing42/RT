#include "libft.h"

void		ft_putnbr(int n)
{
	long	l;
	char	c;

	l = (long)n;
	if (n < 0)
	{
		write(1, "-", 1);
		l = -l;
	}
	if (n > 9 || n < -9)
		ft_putnbr((int)(l / 10));
	c = '0' + (l % 10);
	write(1, &c, 1);
}
